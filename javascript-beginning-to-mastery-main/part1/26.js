// Array is object short intro:

// In JavaScript, the typeof operator returns the type of a variable or expression. When you apply typeof to an array, it returns 'object'. This behavior can be confusing to some because arrays in JavaScript are a special type of object.

// Arrays in JavaScript are a subtype of the object type. While arrays have some unique properties and behaviors compared to generic objects, they are technically objects themselves. This is because arrays in JavaScript are implemented as objects with numeric keys (indices) and special array methods like push(), pop(), slice(), etc.